def build_disposal_nav(result: dict[str, Any], alarm_text: str = "") -> dict[str, Any]:
    itype = str(result.get("incident_type") or "").strip() or "其他"
    tpl = _TEMPLATES.get(itype, _DEFAULT)
    tid, label, steps = tpl[0], tpl[1], tpl[2]
    risk = str(result.get("risk_level") or "中")
    hint = ""
    if risk in ("高", "紧急"):
        hint = "当前风险等级较高：任一步骤中遇失控态势，立即请求增援并汇报指挥中心。"
    return {
        "version": 1,
        "template_id": tid,
        "template_label": label,
        "incident_type": itype,
        "risk_level": risk,
        "risk_hint": hint,
        "steps": list(steps),
    }
